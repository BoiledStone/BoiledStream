create table if not exists public.comments (
  id bigint generated by default as identity primary key,
  video_id text not null,
  user_id uuid not null references public.profiles(id) on delete cascade,
  body text not null check (char_length(trim(body)) between 1 and 1000),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.ratings (
  video_id text not null,
  user_id uuid not null references public.profiles(id) on delete cascade,
  score smallint not null check (score between 1 and 5),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (video_id, user_id)
);

create index if not exists comments_video_id_created_at_idx
  on public.comments (video_id, created_at desc);

create index if not exists ratings_video_id_idx
  on public.ratings (video_id);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create or replace function public.create_profile_for_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, display_name)
  values (
    new.id,
    public.clean_display_name(
      coalesce(new.raw_user_meta_data ->> 'display_name', split_part(new.email, '@', 1))
    )
  )
  on conflict (id) do nothing;

  return new;
end;
$$;

