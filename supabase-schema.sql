create or replace function public.clean_display_name(value text)
returns text
language sql
immutable
as $$
  select coalesce(
    nullif(
      left(
        btrim(
          regexp_replace(
            regexp_replace(coalesce(value, ''), '[^A-Za-z0-9 _-]+', '', 'g'),
            '[[:space:]]+',
            ' ',
            'g'
          )
        ),
        40
      ),
      ''
    ),
    'Utilisateur'
  );
$$;

create or replace function public.is_valid_display_name(value text)
returns boolean
language sql
immutable
as $$
  select value = public.clean_display_name(value)
    and char_length(value) between 1 and 40;
$$;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  display_name text not null check (
    char_length(display_name) between 1 and 40
    and public.is_valid_display_name(display_name)
  ),
  avatar_url text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table if exists public.profiles
  add column if not exists avatar_url text;

update public.profiles
set display_name = public.clean_display_name(display_name)
where not public.is_valid_display_name(display_name);

alter table public.profiles
  drop constraint if exists profiles_display_name_plain_check;

alter table public.profiles
  add constraint profiles_display_name_plain_check
  check (public.is_valid_display_name(display_name));

